package com.carpark.admin.model;

public class EvaluationDto {
	private int penaltyCode;
	private String penaltyName;
	private String penaltyContent;
	private int penaltyPoint;
	
	public int getPenaltyCode() {
		return penaltyCode;
	}
	public void setPenaltyCode(int penaltyCode) {
		this.penaltyCode = penaltyCode;
	}
	public String getPenaltyName() {
		return penaltyName;
	}
	public void setPenaltyName(String penaltyName) {
		this.penaltyName = penaltyName;
	}
	public String getPenaltyContent() {
		return penaltyContent;
	}
	public void setPenaltyContent(String penaltyContent) {
		this.penaltyContent = penaltyContent;
	}
	public int getPenaltyPoint() {
		return penaltyPoint;
	}
	public void setPenaltyPoint(int penaltyPoint) {
		this.penaltyPoint = penaltyPoint;
	}
	
	
}
